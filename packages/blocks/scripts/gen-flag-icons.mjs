// Generates src/flag-icons.ts from flag-icons (rectangular 4:3) + circle-flags (circular),
// both MIT. Run via:  node scripts/gen-flag-icons.mjs
// Country flags are FULL-COLOR (unlike the currentColor Lucide/brand sets), so they get their
// own `{{sw-flag}}` helper. We inline the SVG body for every ISO 3166-1 country in BOTH shapes:
//   {{sw-flag "de"}}         → rectangular 4:3   (flag-icons)
//   {{sw-flag "de-circle"}}  → circular          (circle-flags)
// The published site inlines only the flags a template actually uses; the full set rides the
// render worker + a lazy editor gallery chunk. Re-run to refresh, then commit the generated file.
import { readFileSync, writeFileSync, existsSync } from 'node:fs';
import { dirname, join } from 'node:path';
import { fileURLToPath } from 'node:url';
import { createRequire } from 'node:module';

const require = createRequire(import.meta.url);
const fiDir = dirname(require.resolve('flag-icons/package.json'));
const cfDir = dirname(require.resolve('circle-flags/package.json'));
const countries = require('flag-icons/country.json');

// The SVG elements these two flag sets legitimately use (verified across the whole set). Anything
// outside this allowlist — or a <script>/<style>/on* handler — fails the build, so a future package
// bump can't smuggle active content into the inlined markup. (Compared case-insensitively.)
const ALLOW = new Set(
  ['svg', 'path', 'g', 'rect', 'circle', 'ellipse', 'polygon', 'polyline', 'line', 'defs', 'clipPath', 'mask', 'marker', 'use', 'linearGradient', 'radialGradient', 'stop'].map((t) => t.toLowerCase()),
);

/** Assert the SVG text contains only allowlisted elements and no active content. */
function assertSafe(svg, where) {
  if (/<script|<style|\son[a-z-]+\s*=|javascript:/i.test(svg)) throw new Error(`active content in ${where}`);
  for (const m of svg.matchAll(/<\/?([a-zA-Z][a-zA-Z0-9:-]*)/g)) {
    if (!ALLOW.has(m[1].toLowerCase())) throw new Error(`disallowed <${m[1]}> in ${where}`);
  }
  // Every href/xlink:href (e.g. on <use>) must be a same-document fragment ref (#id) — never an
  // external/data URL (which would be an SSRF on server render + a privacy leak on the client).
  for (const m of svg.matchAll(/(?:xlink:)?href\s*=\s*"([^"]*)"/gi)) {
    if (!m[1].startsWith('#')) throw new Error(`non-fragment href "${m[1]}" in ${where}`);
  }
}

/**
 * Turn a standalone flag SVG into { viewBox, body }: strip the outer <svg> wrapper, keep the inner
 * markup, and NAMESPACE every internal id with a per-(shape,code) prefix so two flags (or a clip
 * mask reused across flags) never collide on one page. Whitespace between tags is collapsed.
 */
function extract(svg, prefix, where) {
  assertSafe(svg, where);
  const vb = /viewBox="([^"]+)"/.exec(svg);
  if (!vb) throw new Error(`no viewBox in ${where}`);
  // Inner markup = everything between the first '>' of <svg …> and the final </svg>.
  let body = svg.slice(svg.indexOf('>', svg.indexOf('<svg')) + 1, svg.lastIndexOf('</svg>'));
  // Collect ids, then rewrite definitions + every reference (url(#id), href="#id").
  const ids = [...body.matchAll(/\bid="([^"]+)"/g)].map((m) => m[1]);
  for (const id of new Set(ids)) {
    const next = `${prefix}-${id}`;
    body = body
      .split(`id="${id}"`).join(`id="${next}"`)
      .split(`url(#${id})`).join(`url(#${next})`)
      .split(`href="#${id}"`).join(`href="#${next}"`);
  }
  return { viewBox: vb[1], body: body.replace(/>\s+</g, '><').replace(/\s{2,}/g, ' ').trim() };
}

// Non-ISO organisation flags to include anyway (rectangle-only — no circular variant exists).
const EXTRA_ORGS = new Set(['asean', 'cefta', 'eac', 'arab', 'pc']);

const entries = [];
let missingCircle = 0;
for (const c of countries) {
  const code = c.code.toLowerCase();
  // ISO 3166-1 countries, plus the handful of organisation flags above; skip subdivision pseudo-flags.
  if (!c.iso && !EXTRA_ORGS.has(code)) continue;
  const rectPath = join(fiDir, 'flags', '4x3', `${code}.svg`);
  if (!existsSync(rectPath)) continue;
  const rect = extract(readFileSync(rectPath, 'utf8'), `r${code}`, `flag-icons/4x3/${code}`);
  const circPath = join(cfDir, 'flags', `${code}.svg`);
  let circle = null;
  if (existsSync(circPath)) circle = extract(readFileSync(circPath, 'utf8'), `c${code}`, `circle-flags/${code}`);
  else missingCircle += 1;
  entries.push({ code, name: c.name, rect, circle });
}
entries.sort((a, b) => a.code.localeCompare(b.code));

const row = (e) =>
  `  ['${e.code}', { name: ${JSON.stringify(e.name)}, rect: { viewBox: '${e.rect.viewBox}', body: ${JSON.stringify(e.rect.body)} }, ` +
  `circle: ${e.circle ? `{ viewBox: '${e.circle.viewBox}', body: ${JSON.stringify(e.circle.body)} }` : 'null'} }],`;

const out = `// AUTO-GENERATED by scripts/gen-flag-icons.mjs from flag-icons (4:3, MIT) + circle-flags
// (circular, MIT). DO NOT EDIT BY HAND — re-run the generator to refresh the set. Each entry is a
// FULL-COLOR flag (its own fills, NOT currentColor) for an ISO 3166-1 country, in two shapes;
// internal ids are namespaced per country+shape so flags never collide on a page.

/** One flag shape: the inner SVG markup plus its viewBox (the helper supplies the <svg> wrapper). */
export interface FlagShape {
  viewBox: string;
  body: string;
}

/** A country flag: English name + a rectangular (4:3) and circular variant (circle may be absent). */
export interface FlagIcon {
  name: string;
  rect: FlagShape;
  circle: FlagShape | null;
}

const FLAG_ICONS = new Map<string, FlagIcon>([
${entries.map(row).join('\n')}
]);

/** ISO 3166-1 alpha-2 codes of the built-in flags (for the editor's flag gallery). */
export const FLAG_CODES: readonly string[] = [...FLAG_ICONS.keys()];

/** The flag for an alpha-2 country code (case-insensitive), or \`undefined\` if unknown. */
export function flagIcon(code: string): FlagIcon | undefined {
  return FLAG_ICONS.get(code.toLowerCase());
}
`;

const target = join(dirname(fileURLToPath(import.meta.url)), '..', 'src', 'flag-icons.ts');
writeFileSync(target, out);
console.log(`wrote ${entries.length} flags to ${target} (${missingCircle} without a circular variant)`);
