// Generates packages/blocks/src/google-fonts-catalog.ts from Google's keyless metadata endpoint
// (the same one fonts.google.com itself uses — no API key). Run: `pnpm gen:google-fonts`.
// We keep only the data the picker + the server-side allowlist need: family, a generic CSS
// fallback (from the Google category), and the NORMAL (non-italic) weights offered. The output
// is a static module so search/browse work fully offline and the woff2 fetch can validate a
// requested family/weight against a fixed allowlist.
import { writeFile } from 'node:fs/promises';
import { fileURLToPath } from 'node:url';
import { dirname, join } from 'node:path';

const OUT = join(dirname(fileURLToPath(import.meta.url)), '..', 'src', 'google-fonts-catalog.ts');

/** Google category → a CSS generic fallback so non-latin / unloaded text degrades sanely. */
const FALLBACK = {
  Serif: 'serif',
  'Sans Serif': 'sans-serif',
  Display: 'sans-serif',
  Handwriting: 'cursive',
  Monospace: 'monospace',
};

const FAMILY_RE = /^[A-Za-z0-9][A-Za-z0-9 '-]*$/; // mirrors the schema FontSlot family regex

const res = await fetch('https://fonts.google.com/metadata/fonts', {
  headers: { 'user-agent': 'sitewright-font-catalog-generator' },
});
if (!res.ok) throw new Error(`metadata fetch failed: ${res.status}`);
const text = await res.text();
const json = JSON.parse(text.replace(/^\)\]\}'/, '')); // strip the anti-hijacking prefix

const fonts = [];
for (const m of json.familyMetadataList ?? []) {
  const family = m.family;
  if (!FAMILY_RE.test(family)) continue; // skip names the schema would reject
  const fallback = FALLBACK[m.category] ?? 'sans-serif';
  // `fonts` is a map keyed by weight; "400i" etc. are italics — keep normal weights only.
  const weights = Object.keys(m.fonts ?? {})
    .filter((w) => /^\d+$/.test(w))
    .map(Number)
    .filter((w) => w >= 100 && w <= 900 && w % 100 === 0)
    .sort((a, b) => a - b);
  if (weights.length === 0) continue;
  fonts.push({ family, fallback, weights });
}
fonts.sort((a, b) => a.family.localeCompare(b.family, 'en'));

const body = `// AUTO-GENERATED by scripts/gen-google-fonts.mjs — do not edit by hand.
// A static, offline catalog of Google Fonts (family + generic fallback + normal weights), used by
// the editor's font picker/library and as the server-side allowlist for the self-hosting fetch.

export interface GoogleFontMeta {
  /** The Google Fonts family name (e.g. "Playfair Display"). */
  family: string;
  /** CSS generic fallback for the family's category (serif/sans-serif/monospace/cursive). */
  fallback: string;
  /** Normal-style weights the family offers (100–900). */
  weights: number[];
}

export const GOOGLE_FONTS: readonly GoogleFontMeta[] = ${JSON.stringify(fonts)};

const BY_FAMILY = new Map(GOOGLE_FONTS.map((f) => [f.family, f] as const));

/** The catalog entry for a family, or undefined if it isn't a known Google font. */
export function googleFont(family: string): GoogleFontMeta | undefined {
  return BY_FAMILY.get(family);
}

/** Whether a family is in the catalog (the allowlist gate for the self-hosting fetch). */
export function isGoogleFamily(family: string): boolean {
  return BY_FAMILY.has(family);
}

/** A slugified, path-safe id for a family (used as the font record id + on-disk dir). */
export function familySlug(family: string): string {
  return family.toLowerCase().replace(/[^a-z0-9]+/g, '-').replace(/^-+|-+$/g, '');
}
`;

await writeFile(OUT, body);
console.log(`wrote ${fonts.length} families → ${OUT}`);
