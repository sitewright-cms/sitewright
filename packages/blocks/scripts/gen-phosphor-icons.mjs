// Generates src/phosphor-icons.ts from @phosphor-icons/core (MIT, https://phosphoricons.com).
// Run via:  node scripts/gen-phosphor-icons.mjs
// Each icon is stored as its six weight bodies (thin, light, regular, bold, fill, duotone) — the inner
// markup of a 256x256, currentColor <svg>; the {{sw-icon}} helper wraps it. Phosphor is a DEVDependency
// used ONLY here: the paths are inlined into the generated .ts so there is no runtime dependency (same
// model as gen-lucide-icons / gen-brand-icons). The publisher only-used-ships the referenced icons.
import { readdirSync, readFileSync, writeFileSync } from 'node:fs';
import { fileURLToPath } from 'node:url';
import { dirname, join } from 'node:path';

const HERE = dirname(fileURLToPath(import.meta.url));
const CORE = join(HERE, '../node_modules/@phosphor-icons/core');
const ASSETS = join(CORE, 'assets');

// The six Phosphor weights. Order matters — it is the index order stored per icon and the row order the
// editor's weight picker renders. `regular` files are `<name>.svg`; every other weight is `<name>-<weight>.svg`.
const WEIGHTS = ['thin', 'light', 'regular', 'bold', 'fill', 'duotone'];

// Extract the inner markup of a Phosphor svg (drop the outer <svg …> wrapper; keep every path incl. the
// duotone secondary's opacity="0.2"). Phosphor bodies are trusted build-time data, never tenant input.
function innerBody(svg) {
  const open = svg.indexOf('>');
  const close = svg.lastIndexOf('</svg>');
  if (open < 0 || close < 0) throw new Error('unexpected phosphor svg shape');
  return svg
    .slice(open + 1, close)
    .replace(/\s+/g, ' ')
    .trim();
}

// The regular weight is the canonical name list.
const names = readdirSync(join(ASSETS, 'regular'))
  .filter((f) => f.endsWith('.svg'))
  .map((f) => f.replace(/\.svg$/, ''))
  .sort();

const rows = [];
for (const name of names) {
  const bodies = WEIGHTS.map((w) => {
    const file = w === 'regular' ? `${name}.svg` : `${name}-${w}.svg`;
    const svg = readFileSync(join(ASSETS, w, file), 'utf8');
    return innerBody(svg);
  });
  rows.push({ name, bodies });
}

const esc = (s) => s.replace(/\\/g, '\\\\').replace(/"/g, '\\"');
const version = JSON.parse(readFileSync(join(CORE, 'package.json'), 'utf8')).version;

const out = `// AUTO-GENERATED by scripts/gen-phosphor-icons.mjs from @phosphor-icons/core@${version} (MIT,
// https://phosphoricons.com). DO NOT EDIT BY HAND — re-run the generator to refresh. Each value is the six
// weight bodies [${WEIGHTS.join(', ')}] — the inner markup of a 256x256, currentColor <svg>. The Icon block
// / {{sw-icon}} helper wraps them. Phosphor is inlined here so there is no runtime dependency; the publisher
// only-used-ships referenced icons. Duotone keeps its secondary path's opacity="0.2" (single-colour duotone;
// set --sw-icon-duo to give the secondary a distinct colour — see the .sw-icon CSS).

/** The Phosphor weights, in picker/row order. */
export const PHOSPHOR_WEIGHTS = ['${WEIGHTS.join("', '")}'] as const;
export type PhosphorWeight = (typeof PHOSPHOR_WEIGHTS)[number];

// name -> the six weight bodies, indexed by PHOSPHOR_WEIGHTS.
const PHOSPHOR = new Map<string, readonly string[]>([
${rows.map((r) => `  ["${r.name}", [${r.bodies.map((b) => `"${esc(b)}"`).join(', ')}]],`).join('\n')}
]);

/** The inner SVG body for an icon at a weight, or undefined if the icon is unknown. */
export function phosphorBody(name: string, weight: PhosphorWeight): string | undefined {
  const bodies = PHOSPHOR.get(name);
  if (!bodies) return undefined;
  const i = PHOSPHOR_WEIGHTS.indexOf(weight);
  return bodies[i < 0 ? PHOSPHOR_WEIGHTS.indexOf('fill') : i];
}

/** Whether a name exists in the Phosphor set. */
export function isPhosphorName(name: string): boolean {
  return PHOSPHOR.has(name);
}

/** Every Phosphor icon name (sorted). */
export const PHOSPHOR_NAMES: readonly string[] = [...PHOSPHOR.keys()];
`;

const dest = join(HERE, '../src/phosphor-icons.ts');
writeFileSync(dest, out);
console.log(`gen-phosphor-icons: wrote src/phosphor-icons.ts (${names.length} icons x ${WEIGHTS.length} weights, ${(out.length / 1024 / 1024).toFixed(1)} MB)`);
